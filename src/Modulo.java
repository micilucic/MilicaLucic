public class Modulo {
    public static void main(String[] args) {
        int firstNumber = 134435;
        int secondNumber = 3;

        int result = firstNumber % secondNumber;

        if (result == 0) {
            System.out.println("Number is divisible by 3");
        } else {
            System.out.println("Number is not divisible by 3");
        }

    }
}
